a = set('abracadabra') 
b = set('alacazam')   
print(a)
print(b)                               
print(a - b)   # Elements of a that are not in b                           
print(a | b )  # union of a and b                         
print(a & b )  # intersection of a and b                          
print(a ^ b )  # unique elements of a and b                           
